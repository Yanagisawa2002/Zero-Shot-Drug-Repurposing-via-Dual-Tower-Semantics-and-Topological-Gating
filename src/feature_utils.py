from __future__ import annotations

import warnings
from pathlib import Path

import torch
from torch import Tensor
from torch_geometric.data import HeteroData


def sanitize_node_type_for_filename(node_type: str) -> str:
    """??????????????????????"""

    return node_type.replace('/', '_').replace(' ', '_').replace('-', '_')


def inject_features_to_graph(data: HeteroData, feature_dir: str | Path) -> HeteroData:
    """
    ??????????????? `HeteroData` ??

    ???
    - ????????????? `{safe_node_type}_features.pt`?
    - ?? `safe_node_type` ?? `/`???? `-` ??? `_`?
    - ?? `gene/protein -> gene_protein_features.pt`?

    ???
    - ??????????????? `data[node_type].x`?
    - ?????????????????????????????
    - ??????????????????????????????
    """

    feature_dir = Path(feature_dir)
    if not feature_dir.exists():
        raise FileNotFoundError(f'????????{feature_dir}')
    if not feature_dir.is_dir():
        raise NotADirectoryError(f'????????????{feature_dir}')

    for node_type in data.node_types:
        safe_node_type = sanitize_node_type_for_filename(node_type)
        feature_path = feature_dir / f'{safe_node_type}_features.pt'

        if not feature_path.exists():
            warnings.warn(
                (
                    f'???? `{node_type}` ?????????{feature_path}?'
                    '???????????????????? embedding?'
                ),
                category=UserWarning,
                stacklevel=2,
            )
            continue

        feature_tensor = torch.load(feature_path, map_location='cpu')
        if not isinstance(feature_tensor, Tensor):
            raise TypeError(
                f'???? `{feature_path}` ?????? torch.Tensor?????? {type(feature_tensor)}?'
            )
        if feature_tensor.dim() != 2:
            raise ValueError(
                f'???? `{feature_path}` ?????? `(num_nodes, feat_dim)`?'
                f'???? {tuple(feature_tensor.shape)}?'
            )

        expected_num_nodes = int(data[node_type].num_nodes)
        if feature_tensor.size(0) != expected_num_nodes:
            raise ValueError(
                f'???? `{node_type}` ???????????????'
                f'expected={expected_num_nodes}, actual={feature_tensor.size(0)}?'
            )

        data[node_type].x = feature_tensor.to(dtype=torch.float32).contiguous()

    return data
